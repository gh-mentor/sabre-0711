# This file contains git commands to stage, commit, and push changes to the 'main' branch of the remote repository.

# Stage all changes using the 'add' command
git add .

# Commit all changes using the 'commit' command
git commit -m "Updated content for demos"

# Push changes to the 'main' branch of the remote repository using the 'push' command
git push origin main

# End of script
